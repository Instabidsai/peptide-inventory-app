export interface SupplementNote {
    name: string;
    dosage: string;
    reason: string;
    productLink?: string;
    productName?: string;
}

export interface DosingTier {
    id: string;          // 'conservative', 'standard', 'aggressive', 'loading', 'maintenance'
    label: string;       // Display label: "Conservative Start", "Standard Protocol", etc.
    doseAmount: number;
    doseUnit: 'mg' | 'mcg' | 'iu';
    frequency: string;
    timing: string;
    notes?: string;
    dosageSchedule?: string;
    cyclePattern?: string;
    cyclePatternOptions?: string[];
}

export interface PeptideKnowledge {
    description: string;
    vialSizeMg: number;
    reconstitutionMl: number;
    defaultDoseAmount: number;
    defaultDoseUnit: 'mg' | 'mcg' | 'iu';
    defaultFrequency: string;
    defaultTiming: string;
    administrationRoute: string;
    warningText?: string;
    cyclePattern?: string;
    cyclePatternOptions?: string[];
    stackGroup?: string;
    stackLabel?: string;
    supplementNotes?: SupplementNote[];
    dosageSchedule?: string;
    dosingTiers?: DosingTier[];
    category?: 'healing' | 'gh_stack' | 'weight_loss' | 'cognitive' | 'sleep' | 'anti_aging' | 'sexual_health';
}

